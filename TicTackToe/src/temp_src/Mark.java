/**
 * The Mark enum represents the possible marks that can be placed on a game board.
 * It includes BLANK, X, and O as the three types of marks.
 *
 * @author Yotam
 */
public enum Mark {
    /**
     * Represents an empty cell on the game board.
     */
    BLANK,

    /**
     * Represents the X mark on the game board.
     */
    X,

    /**
     * Represents the O mark on the game board.
     */
    O
}
